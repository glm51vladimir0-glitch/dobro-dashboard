def get_region_population(region_name, year):
    """Get population for a region in a given year (thousands)."""
    reg = REGION_POPULATION.get(region_name)
    if reg is None:
        return None
    return reg.get(year)


def get_region_population_series(region_name, years):
    """Get population series for a region (thousands)."""
    reg = REGION_POPULATION.get(region_name)
    if reg is None:
        return [None] * len(years)
    return [reg.get(y) for y in years]


def get_national_population_series(years):
    """Get total national population series (thousands)."""
    return [NATIONAL_POPULATION.get(y) for y in years]


def get_age_group_population(age_group, year):
    """Get national population for an age group in a given year (thousands)."""
    share = AGE_GROUP_SHARES.get(age_group, {}).get(year)
    total = NATIONAL_POPULATION.get(year)
    if share is None or total is None:
        return None
    return round(total * share)


def get_age_group_population_series(age_group, years):
    """Get national population series for an age group (thousands)."""
    return [get_age_group_population(age_group, y) for y in years]
